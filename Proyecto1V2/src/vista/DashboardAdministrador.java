    private void rellenarPanel(JPanel panel, String nombre) {

        panel.setSize(740, 480);
        panel.setLocation(0, 0);

        container.removeAll();
        container.add(panel, BorderLayout.CENTER);
        container.revalidate();
        container.repaint();
        this.setTitle(nombre);
    }

